package Monopoly;
import java.util.*;
public class Giocatore {
	
	private String nome;
	private int posizione;
	
	
	public Giocatore(String nome, int posizione) {
		this.nome=nome;
		this.posizione=posizione;
	}
	
	public String getNome() {
		return nome;
	}
	
	public int getPosizione() {
		return posizione;
	}
	
	



}
